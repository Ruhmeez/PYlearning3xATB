# Ans: "=" is an Assignment operator used to assign value to the variable.
#     "==" is a relational operator used to check the equality of two values.

# b. What does the ** operator do in Python, and how is it used?
# Ans: "**" is called Exponentiation operator, used to get the power of the given number.
# Eg: 4**2 -> 4 is base and 2 is power, the result is 16


# c. What does the ^ operator do in Python, and in what context is it commonly used?
# The bitwise XOR (exclusive OR) operator compares each bit of two integers and returns a new integer with bits set to 1 if the corresponding bits in the operands are different. It performs a bitwise "exclusive or" operation.
# Ex:
# a = 5   # binary: 0101
# b = 3   # binary: 0011
# result = a ^ b  # binary result: 0110 (decimal 6)










