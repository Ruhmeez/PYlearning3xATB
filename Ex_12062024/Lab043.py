# for loop

for i in range(5): # 0 to 4
    print(i)

for x in range(1,5): # 1 to 4
    print(x)

for x in range(3,5): # 3 to 4
    print(x)

# Range -> (start, stop, step)

for i in range(1, 10, 1):
    print(i)

for i in range(1, 10, 2):
    print(i)


